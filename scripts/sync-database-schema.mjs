import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = join(__dirname, '..');
const outputDir = join(rootDir, 'database-schema');
const tablesDir = join(outputDir, 'tables');

function readEnvFile(path) {
  if (!existsSync(path)) return {};

  return Object.fromEntries(
    readFileSync(path, 'utf8')
      .split(/\r?\n/)
      .map(line => line.trim())
      .filter(line => line && !line.startsWith('#') && line.includes('='))
      .map(line => {
        const index = line.indexOf('=');
        return [line.slice(0, index), line.slice(index + 1).trim().replace(/^"|"$/g, '')];
      })
  );
}

const env = {
  ...readEnvFile(join(rootDir, '.env.local')),
  ...readEnvFile(join(rootDir, '.env.mcp.local')),
  ...process.env,
};

const supabaseUrl = env.MCP_SUPABASE_URL ?? env.SUPABASE_URL ?? env.VITE_SUPABASE_URL;
const serviceRoleKey = env.MCP_SUPABASE_SERVICE_ROLE_KEY ?? env.SUPABASE_SERVICE_ROLE_KEY;

function requireConfig() {
  if (!supabaseUrl || !serviceRoleKey) {
    throw new Error(
      'Missing MCP_SUPABASE_URL/VITE_SUPABASE_URL or MCP_SUPABASE_SERVICE_ROLE_KEY. Create .env.mcp.local from .env.mcp.example.'
    );
  }

  if (serviceRoleKey === env.VITE_SUPABASE_ANON_KEY) {
    throw new Error('MCP_SUPABASE_SERVICE_ROLE_KEY must be the service_role key, not the public anon key.');
  }
}

function markdownEscape(value) {
  return String(value ?? '').replaceAll('|', '\\|').replaceAll('\n', '<br>');
}

function slugify(value) {
  return value.replace(/[^a-zA-Z0-9_.-]/g, '-');
}

function getColumnType(property) {
  if (!property) return 'unknown';
  if (property.format) return `${property.type ?? 'unknown'}:${property.format}`;
  if (property.type) return property.type;
  if (property.$ref) return property.$ref.split('/').pop();
  if (Array.isArray(property.oneOf)) return property.oneOf.map(getColumnType).join(' | ');
  if (Array.isArray(property.anyOf)) return property.anyOf.map(getColumnType).join(' | ');
  return 'unknown';
}

function getColumnDefault(property) {
  if (!property || !Object.hasOwn(property, 'default')) return '';
  if (property.default === '') return '""';
  if (typeof property.default === 'object') return JSON.stringify(property.default);
  return String(property.default);
}

function extractPrimaryKeys(definitions) {
  const primaryKeys = [];

  for (const [tableName, definition] of Object.entries(definitions)) {
    for (const [columnName, property] of Object.entries(definition.properties ?? {})) {
      if (property.description?.includes('<pk/>')) {
        primaryKeys.push({ tableName, columnName });
      }
    }
  }

  return primaryKeys;
}

function extractForeignKeys(definitions) {
  const foreignKeys = [];
  const regex = /<fk table='([^']+)' column='([^']+)'\/>/;

  for (const [tableName, definition] of Object.entries(definitions)) {
    for (const [columnName, property] of Object.entries(definition.properties ?? {})) {
      const match = property.description?.match(regex);
      if (match) {
        foreignKeys.push({
          tableName,
          columnName,
          targetTable: match[1],
          targetColumn: match[2],
        });
      }
    }
  }

  return foreignKeys;
}

function formatTableMarkdown(tableName, definition) {
  const properties = definition.properties ?? {};
  const required = new Set(definition.required ?? []);
  const columns = Object.entries(properties);

  const lines = [
    '<!-- Generated by npm run db:schema:sync. Do not edit by hand. -->',
    '',
    `# ${tableName}`,
    '',
    definition.description ? `${definition.description}\n` : '',
    `Column count: ${columns.length}`,
    '',
    '| Column | Type | Required | Nullable | Default | Description |',
    '| --- | --- | --- | --- | --- | --- |',
  ];

  for (const [columnName, property] of columns) {
    lines.push(
      `| ${markdownEscape(columnName)} | ${markdownEscape(getColumnType(property))} | ${
        required.has(columnName) ? 'yes' : 'no'
      } | ${property.nullable ? 'yes' : 'no'} | ${markdownEscape(getColumnDefault(property))} | ${markdownEscape(
        property.description ?? ''
      )} |`
    );
  }

  lines.push('');
  lines.push('## Raw Definition');
  lines.push('');
  lines.push('```json');
  lines.push(JSON.stringify(definition, null, 2));
  lines.push('```');
  lines.push('');

  return lines.filter(line => line !== undefined).join('\n');
}

function formatOverviewMarkdown(spec, definitions) {
  const tableNames = Object.keys(definitions).sort((a, b) => a.localeCompare(b));
  const generatedAt = new Date().toISOString();

  const lines = [
    '<!-- Generated by npm run db:schema:sync. Do not edit by hand. -->',
    '',
    '# Database Schema Overview',
    '',
    `Generated at: ${generatedAt}`,
    '',
    `Supabase project: ${supabaseUrl}`,
    '',
    `OpenAPI title: ${spec.info?.title ?? 'unknown'}`,
    `OpenAPI version: ${spec.info?.version ?? 'unknown'}`,
    '',
    `Exposed tables/views: ${tableNames.length}`,
    '',
    '| Table | Columns | File |',
    '| --- | ---: | --- |',
  ];

  for (const tableName of tableNames) {
    const columnCount = Object.keys(definitions[tableName]?.properties ?? {}).length;
    lines.push(`| ${markdownEscape(tableName)} | ${columnCount} | [${tableName}](./tables/${slugify(tableName)}.md) |`);
  }

  lines.push('');
  lines.push('## Notes For AI');
  lines.push('');
  lines.push('- This folder is a schema snapshot, not a source of truth. Supabase remains the source of truth.');
  lines.push('- Regenerate after database structure changes with `npm run db:schema:sync`.');
  lines.push('- These files intentionally contain database shape only, not table rows or secrets.');
  lines.push('- If a table is missing here, check whether it is exposed through the Supabase Data API.');
  lines.push('- See `relationships.md` for extracted primary-key and foreign-key hints.');
  lines.push('');

  return lines.join('\n');
}

function formatRelationshipsMarkdown(definitions) {
  const primaryKeys = extractPrimaryKeys(definitions).sort((a, b) =>
    `${a.tableName}.${a.columnName}`.localeCompare(`${b.tableName}.${b.columnName}`)
  );
  const foreignKeys = extractForeignKeys(definitions).sort((a, b) =>
    `${a.tableName}.${a.columnName}`.localeCompare(`${b.tableName}.${b.columnName}`)
  );

  const lines = [
    '<!-- Generated by npm run db:schema:sync. Do not edit by hand. -->',
    '',
    '# Database Relationships',
    '',
    'This file extracts relationship hints from the Supabase REST OpenAPI schema.',
    '',
    '## Primary Keys',
    '',
    '| Table | Column |',
    '| --- | --- |',
  ];

  for (const key of primaryKeys) {
    lines.push(`| ${markdownEscape(key.tableName)} | ${markdownEscape(key.columnName)} |`);
  }

  lines.push('');
  lines.push('## Foreign Keys');
  lines.push('');
  lines.push('| From | To |');
  lines.push('| --- | --- |');

  for (const key of foreignKeys) {
    lines.push(
      `| ${markdownEscape(`${key.tableName}.${key.columnName}`)} | ${markdownEscape(
        `${key.targetTable}.${key.targetColumn}`
      )} |`
    );
  }

  lines.push('');

  return lines.join('\n');
}

async function fetchOpenApiSpec() {
  requireConfig();

  const endpoint = `${supabaseUrl.replace(/\/$/, '')}/rest/v1/`;
  const response = await fetch(endpoint, {
    headers: {
      apikey: serviceRoleKey,
      authorization: `Bearer ${serviceRoleKey}`,
      accept: 'application/openapi+json',
    },
  });

  if (!response.ok) {
    throw new Error(`OpenAPI request failed: ${response.status} ${await response.text()}`);
  }

  return response.json();
}

async function main() {
  const spec = await fetchOpenApiSpec();
  const definitions = spec.definitions ?? {};
  const tableNames = Object.keys(definitions).sort((a, b) => a.localeCompare(b));

  mkdirSync(outputDir, { recursive: true });
  rmSync(tablesDir, { recursive: true, force: true });
  mkdirSync(tablesDir, { recursive: true });

  writeFileSync(join(outputDir, 'openapi.json'), `${JSON.stringify(spec, null, 2)}\n`);
  writeFileSync(join(outputDir, 'overview.md'), formatOverviewMarkdown(spec, definitions));
  writeFileSync(join(outputDir, 'relationships.md'), formatRelationshipsMarkdown(definitions));

  for (const tableName of tableNames) {
    writeFileSync(join(tablesDir, `${slugify(tableName)}.md`), formatTableMarkdown(tableName, definitions[tableName]));
  }

  console.log(`Synced ${tableNames.length} schema files into database-schema/tables.`);
}

main().catch(error => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
